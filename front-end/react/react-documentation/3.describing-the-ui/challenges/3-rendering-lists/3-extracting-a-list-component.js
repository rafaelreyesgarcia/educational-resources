const recipes = [{
  id: 'greek-salad',
  name: 'Greek Salad',
  ingredients: ['tomatoes', 'cucumber', 'onion', 'olives', 'feta']
}, {
  id: 'hawaiian-pizza',
  name: 'Hawaiian Pizza',
  ingredients: ['pizza crust', 'pizza sauce', 'mozzarella', 'ham', 'pineapple']
}, {
  id: 'hummus',
  name: 'Hummus',
  ingredients: ['chickpeas', 'olive oil', 'garlic cloves', 'lemon', 'tahini']
}];

import { recipes } from './data.js';

/* 
challenge
recipelist component contains two nested map calls

extract a recipe component that will accept id, name and ingredients props

where to place the outer key and why?
*/

export default function RecipeListChallenge() {
  return (
    <div>
      <h1>Recipes</h1>
      {recipes.map(recipe =>
        <div key={recipe.id}>
          <h2>{recipe.name}</h2>
          <ul>
            {recipe.ingredients.map(ingredient =>
              <li key={ingredient}>
                {ingredient}
              </li>
            )}
          </ul>
        </div>
      )}
    </div>
  );
}

function Recipe({ id, name, ingredients }) {
  return (
    <div>
      <h2>{name}</h2>
      <ul>
        {ingredients.map(ingredient =>
          <li key={ingredient}>
            {ingredient}
          </li>
        )}
      </ul>
    </div>
  );
}

export default function RecipeListSolution() {
  return (
    <div>
      <h1>Recipes</h1>
      {recipes.map(recipe =>
        <Recipe {...recipe} key={recipe.id} />
      )}
    </div>
  );
}

/* 
the Recipe component accepts spread syntax {...recipe}
the recipe is an object, the object properties can be spread with {...recipe}

shorter alternative to
<Recipe 
  id={recipe.id} 
  name={recipe.name}
  ingredients={recipe.ingredients}
  key={recipe.id}
/>

the key is defined on Recipe instead of the root div returned from recipe

when extracting a component, don't forget to leave the key outisde the JSX 
*/

